# Armazenando os valores nas variáveis
var1 = 5
var2 = 2

# Imprimindo o tipo de dados de cada variável
print(type(var1))  # <class 'int'>
print(type(var2))  # <class 'int'>

# Realizando a divisão e armazenando o resultado em uma terceira variável
resultado = var1 / var2

# Imprimindo a terceira variável e seu tipo
print(resultado)    # 2.5
print(type(resultado))  # <class 'float'>
